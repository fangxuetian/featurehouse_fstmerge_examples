

package org.jfree.chart.ui;


public class Contributor {

    
    private String name;

    
    private String email;

    
    public Contributor(String name, String email) {
        this.name = name;
        this.email = email;
    }

    
    public String getName() {
        return this.name;
    }

    
    public String getEmail() {
        return this.email;
    }

}
